export function formatSalary(min: number, max: number, currency: string): string {
  const fmt = (n: number) =>
    n >= 1000 ? `${currency === "USD" ? "$" : "€"}${Math.round(n / 1000)}k` : `${n}`;
  return `${fmt(min)}–${fmt(max)}`;
}
